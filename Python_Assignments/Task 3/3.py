# 3. Write a program to get the sum and multiply of all the items in a given list.

list = [10, 100, 1000, 10000, 100000, 10000, 1000, 100, 10]
print("Given List: ", list)
print("The sum and Multiplications of all the numbers in above list:")
sum = 0
mult = 1

for i in list:
    sum = sum+i
    mult = mult*i

print("sum: ", sum,"\nMultiplication: ", mult)
















